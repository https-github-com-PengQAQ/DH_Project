			    UIManager.instance = (UIManager)translator.GetObject(L, 1, typeof(UIManager));
            
            } catch(System.Exception gen_e) {
                return LuaAPI.luaL_error(L, "c# exception:" + gen_e);
            }
            return 0;
        }
        
		
		
		
		
